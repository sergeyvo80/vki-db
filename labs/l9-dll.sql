-- 2. Создать таблицу «Страны_ВашаФамилия». Определить основной ключ, разреше-
-- ние / запрет на NULL, условие на вводимое значение.
CREATE TABLE Страны_Овчинников (
  ID INT PRIMARY KEY CHECK (ID < 200),
  field1 VARCHAR(20) NOT NULL,
  field2 VARCHAR(20) NULL,
);

-- 3. Создать таблицу «Цветы_ВашаФамилия». Определить основной ключ, значения
