);

-- 4. Создать таблицу «Животные_ВашаФамилия». Определить основной ключ, значе-
-- ния столбца «ID» сделать уникальными, для столбца «Отряд» установить значение по умол-
-- чанию «Хищные».
CREATE TABLE Животные_Овчинников (
